package com.jivesoftware.it.vidyo;

public class ErrorInfo {
    public static final int CODE_SERVER_ERROR = 1;
    public static final int CODE_PARTICIPANT_OFFLINE = 2;
    public static final int CODE_UNKNOWN_USER = 3;

    private int code;
    private String message;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}

